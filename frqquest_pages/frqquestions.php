                        <h3>Do I Have To Take Any Vitamins?</h3>
                        <p>Even though some women think that it's enough to eat a lot of fresh fruits and veggies during pregnancy, specialists recommend taking supplements as well. Pregnancy isn't easy on the body. Just think about it: there's a brand new organism growing inside the body, and this organism needs a lot of vitamins and minerals for healthy growth.

Therefore, to ensure that the baby gets enough of everything it needs, a woman should at least take additional calcium, folic acid, and iron. Of course, the exact kind of supplements to take has to be discussed with an OB-GYN, because different women might require different formulations.
</p>
