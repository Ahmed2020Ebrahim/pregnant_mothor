           <h3>6th Month Of Pregnancy Diet – What To Avoid?</h3>
           <p>Many women often have cravings for rather unhealthy foods while they are pregnant. While it is okay to indulge moderately in some junk food, there are certain foods that should be avoided at all costs while you are pregnant, especially up to your 6th month.
            Here are some of the foods that are an absolute no-no during your pregnancy:
            </p>
           <strong>1. Raw Seafood:</strong>
           <p>You may have a sudden hankering for sushi but avoid it at all costs! As raw fish carry high level of methylmercury, that can cause food-borne diseases like listeriosis which can be dangerous for your unborn baby.</p><br>
           <strong>2. Undercooked Meat:</strong>
           <p>Always make sure that you cook your meat well as the bacteria mentioned above can be present in meat that is undercooked. If you are cooking meat for a meal, take care to cook it well so that you can digest it easily and also avoid the risk of listeriosis. Try and avoid eating meat outside.</p><br>
           <strong>3. Non-Pasteurized Cheeses:</strong>
           <p>Avoid soft cheeses that are not pasteurized and stick to hard cheeses only during your pregnancy, especially during your 6th month. Disease-causing bacteria can also be present in this type of cheese so omit it from your diet.</p><br>
           <strong>4. Spicy Food:</strong>
           <p>There will be times when you want to eat something hot and spicy; however, it is best to avoid anything that is too spicy. It can cause your stomach to burn and cause indigestion and great discomfort. It is also not one of the best flavors to give your baby, so curb your cravings for anything too spicy.</p><br>
           <strong>5. Caffeine:</strong>
           <p>Needless to say, it is best to avoid caffeinated beverages such as coffee and aerated drinks. It is a much healthier option to drink herbal tea as it will sooth and calm you, which is exactly what you need while you are pregnant. Then again, check with your doctor, as some women are put off green or herbal tea during pregnancy.</p><br>
           <strong>6. Alcohol And Tobacco:</strong>
           <p>It is no secret that you need to completely stay away from alcohol and tobacco while you are pregnant. The damage done to your baby can be irreparable, even fatal, when you smoke or drink during pregnancy.</p><br><br><br>
