            <p>Both tea and coffee contain caffeine. So, you must restrict your tea/coffee intake to a maximum 2 cups a day. High caffeine intake during pregnancy is known to cause restlessness and sleeplessness in babies after they are born. You certainly don’t want your baby to wail endlessly through the initial days after it is born. Unfortunately, chocolate too contains caffeine and I know it’s hard to resist the urge, but you have to.</p><br>
