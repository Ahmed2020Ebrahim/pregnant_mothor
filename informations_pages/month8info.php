            <h3>The nonstress test (NST)</h3>
            <p>The nonstress test (NST) involves a fetal monitor strapped to the mother's abdomen to measure the baby's heart rate as it moves. It is called "nonstress" because no stress is placed on the fetus for the test. This test is sometimes performed on a weekly basis in high-risk pregnancies. It is done after the 28th week of pregnancy, though most often after 32 weeks. Measurements are typically taken for 20 to 30 minutes. A NST may be ordered if you feel the baby is not moving normally, if you are past your due date, or if your doctor wants to ensure that the placenta is healthy and functioning well. There are no known risks of the NST for the mother or the baby.</p><br>
            <strong>A biophysical profile</strong>
            <p>A biophysical profile combines the information from a NST with an ultrasound examination of the baby for a more precise evaluation.</p><br>
            <strong>A contraction stress test (CST)</strong>
            <p>A contraction stress test (CST), like the NST, measures fetal heart rate. However in this test, the baby's heart rate is measured in response to uterine contractions that are elicited by administering oxytocin (Pitocin) or by stimulation of the nipples. The test is sometimes referred to as an oxytocin challenge test. Normally, the flow of blood to the placenta slows during contractions, but if the placenta is functioning well, the baby's heart rate remains stable. If there is poor function of the placenta, the baby's heart rate will temporarily slow after a contraction. Looking at the baby's heart rate in response to uterine contractions can help the doctor estimate how the baby will respond to the stresses experienced during labor. This test is not performed as commonly as the NST or biophysical profile.</p>

