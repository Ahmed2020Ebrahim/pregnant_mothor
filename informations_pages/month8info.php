                <h3>Sports</h3><br><br>
            </div>
                
            <h3>Benefits of doing sport and exercise</h3>
            <p>If you are healthy and have an uncomplicated pregnancy, playing sport can help you stay fitter and stronger, prevent excess weight gain and make you feel better mentally. It may also allow you to have a shorter birth and fewer complications.
            Regular exercise also lowers your risk of heart disease, type 2 diabetes and some cancers.
            But while pregnant, you’ll need to be more careful as well as avoid certain types of sport.
            </p>
            <h3>Why do I need to be more careful?</h3>
            <p>Although physical activity in pregnancy is safe and good for you, some sports may cause harm to you or your baby. Even in sports that are not risky you may need to make some changes. It’s important to tell your maternity team about any sport you are playing so they can support you.</p><br>
            <h3>Medical conditions that may affect you playing sport</h3>
            <p>Your maternity team may want to monitor your sport or advise you against participating, if you:</p>
            <ul>
                <li>have problems with your heart, lungs or liver</li>
                <li>have diabetes that is not well controlled</li>
                <li>have bone or joint problems</li>
                <li>have an infectious disease</li>
                <li>are obese or underweight</li>
                <li>have problems with your pregnancy or a previous pregnancy</li>
                <li>have anaemia</li>
                <li>are having a multiple birth (e.g. twins or triplets)</li>
            </ul>
            <p>When you are exercising, you should stop and see your doctor straight away, if:</p>
            <ul>
                <li>you have chest pain</li>
                <li>your heart rate is higher than usual</li>
                <li>you have unusual shortness of breath</li>
                <li>your baby’s movements have decreased</li>
                <li>you are having contractions</li>
                <li>you leak or gush fluid from your vagina</li>
                <li>your ankles, hands or face suddenly swell</li>
                <li>you have pain, swelling or redness in your calves</li>
                <li>you have muscle weakness</li>
                <li>you feel dizzy or faint</li>
            </ul>
            <h3>Sport and your level of fitness</h3>
            <p>If you are not currently active and starting a sport, it’s good to choose one which needs less effort. You could start with 15 minutes per session, building up to 30 minutes of moderate intensity. If you’ve been inactive, it’s also a good idea consult your doctor before starting to play a sport during pregnancy.
            If you are already active, healthy and have an uncomplicated pregnancy, you can continue to play sport unless it is one that should be avoided during pregnancy. However, this is not a good time to set new personal records.
            It’s important for you to stay comfortable and to warm up before and cool down after your sport.
            </p>
            <h3>Sports to avoid</h3>
            <p>You should not play sports that:</p>
            <ul>
                <li>make you work very hard or become too hot</li>
                <li>could cause you to fall</li>
                <li>could hit your baby</li>
                <li>have lots of jumping or bouncing</li>
                <li>involve sudden changes in direction</li>
                <li>involve diving or high altitudes</li>
            </ul>
            <h3>Sport and stages of pregnancy</h3>
            <p>You may need to make some changes as your pregnancy advances.</p>
            <h3>First trimester (1-12 weeks)</h3>
            <p>During this time, try not to overheat so as to protect both yourself and your baby:</p>
            <ul>
                <li>Avoid playing sport in high heat or humidity.</li>
                <li>Wear loose, cool clothing.</li>
                <li>Drink plenty of water</li>
            </ul>
            <h3>Second and third trimesters (13-40 weeks)</h3>
            <p>
                During this time, the baby moves upwards and is not protected by your pelvis so avoid high impact sports that could hit the baby.
                Your own body weight also moves forward so there is an increased risk of falling. If you feel unsteady or uncomfortable, stop what you’re doing.
                Your ligaments become looser so there is more chance of ligament injuries. Avoid sports that involve heavy weights or sudden movements and changes in direction such as court sports like tennis.
                Your blood pressure may decrease so move more slowly to avoid getting dizzy when changing your posture (for example, from sitting to standing).
                After 16 weeks, avoid activity that involves lying on your back since this affects blood supply to the baby and can make you feel dizzy.
            </p>
            <h3>What sport is recommended?</h3>
            <p>It’s good to do lower-risk activities that:</p>
            <ul>
                <li>are non-contact or limited contact</li>
                <li>support your weight, such as swimming and aquanatal exercise classes — but try to avoid being in water that is warmer than 32 degrees Celsius for very long</li>
                <li>allow you to exercise in a straight line rather than suddenly shifting positions</li>
                <li>have a limited chance of you falling</li>
                <li>allow you to exercise at low or moderate intensity</li>
                <li>are designed specifically for pregnancy</li>
            </ul>


