           <p>Apart from these foods, you should also avoid taking any over the counter medication. If you have a condition that requires you to take medication, consult your doctor and find out if it is safe to be taken while you are pregnant. Only the medicines and supplements prescribed by your doctor should be taken and it is of utmost importance to find out if any other medicines you are taking are safe for your baby. Make sure you make it very clear to your doctor that you are pregnant.</p>
