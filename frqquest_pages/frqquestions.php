                        <h3>I'm Going To My First Check-Up. What Should I Expect?</h3>
                        <p>When a pregnant woman comes to a check-up for the first time, she can expect to have a lengthy appointment. It's likely to include a medical history review, a blood and urine test, pap smear, and a general health check to make sure that the woman has no problems with her overall health.

The specialist will also want to discuss the woman's lifestyle and dietary habits and inform her of the changes she has to implement to ensure proper growth of the baby.
</p>
