           <h3>Amniocentesis</h3>
           <p>Although amniocentesis (removal of a sample of amniotic fluid with a long, thin needle) is most often done during the second trimester, there are certain conditions that may warrant an amniocentesis later in pregnancy. These conditions include a suspicion of chorioamnionitis or a risk of premature delivery, since amniocentesis fluid can be used to estimate maturity of the fetal lungs.</p><br>
