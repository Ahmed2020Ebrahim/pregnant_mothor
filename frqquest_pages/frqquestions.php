                        <p>Some pregnant women avoid eating peanut butter because they don't want to cause an allergy in their future baby. However, this risk exists only if the future mother or father has any food allergies, eczema, hay fever, or asthma. If neither of the parents has these conditions, peanut butter, most likely, won't harm the child.

In the meantime, this issue still hasn't been examined thoroughly. So if you don't mind living without peanut butter for nine months, don't eat it, just to be on the safe side.
