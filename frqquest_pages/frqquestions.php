                       <h3>Is It Unsafe To Sleep On My Back?</h3>
                       <p>Yes, sleeping on the back can be unsafe in the second or third trimester, because the uterus and the baby are growing and their increasing weight can press the main vein that carries blood from the woman's lower body area to her heart. If this vein remains pressed for a prolonged period of time, blood circulation to the fetus can become hampered, and it may prevent proper development of the baby.

It's better for pregnant women to sleep on the side. Specialists also recommend putting "a thin pillow under your right side and tilt your body to the left" to decrease the compression of blood vessels.
</p>
